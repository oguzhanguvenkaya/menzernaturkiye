  if (name.includes("yeni 400") || (name.includes("400") && name.includes("ağır") && !name.includes("yeşil"))) return "heavy_cut_400";
  if (name.includes("cut force")) return "cut_force_pro";
  if (name.includes("1000") && name.includes("kalın")) return "heavy_cut_1000";
  if (name.includes("1100")) return "heavy_cut_1100";
  if (name.includes("300") && name.includes("süper ağır")) return "super_heavy_cut_300";
  if (name.includes("as 30") || name.includes("as30")) return "as30";

  if (name.includes("2500")) return "medium_cut_2500";
  if (name.includes("2200")) return "medium_cut_2200";
  if (name.includes("3in1") || name.includes("3 in 1") || name.includes("tek adım pasta cila koruma")) return "one_step_3in1";

  if (name.includes("3000")) return "final_finish_3000";
  if (name.includes("3500")) return "super_finish_3500";
  if (name.includes("3800")) return "super_finish_3800";

  if (name.includes("sealing wax")) return "sealing_wax";
  if (name.includes("power lock")) return "power_lock";
  if (name.includes("ceramic spray")) return "ceramic_spray";
  if (name.includes("endless shine")) return "endless_shine";
  if (name.includes("likit matlaştırıcı") || name.includes("liquid sanding")) return "liquid_matting";

  if (name.includes("ağır kesim") && name.includes("sünger") && name.includes("150")) return "heavy_cut_foam_pad";
  if (name.includes("ağır kesim") && name.includes("sünger") && name.includes("95")) return "heavy_cut_foam_pad";
  if (name.includes("ince kesim") && name.includes("sünger") && name.includes("150")) return "soft_cut_foam_pad";
  if (name.includes("ince kesim") && name.includes("sünger") && name.includes("95")) return "soft_cut_foam_pad";
  if (name.includes("hare") && name.includes("sünger") && name.includes("150")) return "medium_cut_foam_pad";
  if (name.includes("hare") && name.includes("sünger") && name.includes("95")) return "medium_cut_foam_pad";
  if (name.includes("p150m")) return "standard_pad_p150m";

  if (name.includes("krater") && name.includes("kuzu")) return "crater_lambswool";
  if (name.includes("orbital") && name.includes("kuzu")) return "orbital_wool";
  if (name.includes("premium") && name.includes("kuzu")) return "premium_lambswool";

  if (name.includes("metal polish") || name.includes("metal krom")) return "metal_polish";
  if (name.includes("tabanlık") || name.includes("destek disk")) return "premium_backing_plate";

  if (name.includes("gw16")) return "gw16";
  if (name.includes("gw18")) return "gw18";
  if (name.includes("113gz") || name.includes("113gZ")) return "113gz";
